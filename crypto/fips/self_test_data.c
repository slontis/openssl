/* Binary data */
typedef struct item_st {
    const unsigned char *data;
    int len;
} ITEM;

/* Self test data for a cipher - last fields required for authenticated modes */
typedef struct st_cipher_st {
    int nid;
    int flags;
    ITEM pt;
    ITEM ct_ka;
    ITEM key;
    ITEM iv;
    ITEM add;
    ITEM tag;
} ST_CIPHER;

/* Self test data for a digest */
typedef struct st_digest_st {
    int nid;
    ITEM pt;
    ITEM digest_ka;
} ST_DIGEST;

/* Self test data for a signature */
typedef struct st_sig_st {
    int key_nid;
    int digest_nid;
    ITEM *key;
    ITEM msg;
    ITEM sig_ka;
} ST_SIGNATURE;

/* Self test data for a DRBG */
typedef struct st_drbg_st {
    int nid;
    int flags;
    ITEM init_entropy;
    ITEM pers_str;
    ITEM gen_addin;
    ITEM gen_ka;
    ITEM reseed_entropy;
    ITEM reseed_addin;
    ITEM reseed_ka;
} ST_DRBG;


/* Macros to build Self test data */
#define ITM(x) { x, sizeof(x) }
#define ITM_STR(x) { x, sizeof(x) - 1 }

#define CIPHER_DATA(type, pad) { NID_##type, pad, \
    ITM(type##_pt), ITM(type##_ct), ITM(type##_key), ITM(type##_iv), \
}

#define CIPHER_DATA_STR(type, pad) { NID_##type, pad, \
    ITM_STR(type##_pt), ITM(type##_ct), ITM(type##_key), ITM(type##_iv), \
}

#define CIPHER_KW_DATA(type, pad) { NID_id_##type, pad, \
    ITM(type##_key), ITM(type##_wrapped_key), ITM(type##_kek) \
}

#define CIPHER_AEAD_DATA(type, pad) { NID_##type, pad, \
    ITM(type##_pt), ITM(type##_ct), ITM(type##_key), ITM(type##_nonce), \
    ITM(type##_adata), ITM(type##_tag) \
}

#define DIGEST_DATA(type) { NID_##type, ITM(type##_pt), ITM(type##_digest) }

#define DRBG_DATA(type, name, flags) { NID_##type, flags, \
    ITM(name##_init_entropy), ITM(name##_pers_str), ITM(name##_addin), \
    ITM(name##_gen_kat), \
    ITM(name##_reseed_addin), ITM(name##_reseed_entropy), \
    ITM(name##_reseed_kat) \
}

